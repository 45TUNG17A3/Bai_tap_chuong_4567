x = float(input("Nhập vào giá trị của x: "))
y = float(input("Nhập vào giá trị của y: "))
z = float(input("Nhập vào giá trị của z: "))
if x>y and x>z:
    print("Số lớn nhất là x:", x)
elif y>x and y>z:
    print("Số lớn nhất là y:", y)
elif z>x and z>y:
    print("Số lớn nhất là z:", z)        

            